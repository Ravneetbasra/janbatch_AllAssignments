public class Class1 {

	
		
		public int sum(int x, int y) {
			
			int z = x+y;
			
			return z;
		}
		
		public int sub(int x, int y) {
			
			int z = x-y;
			return z;
		}
		
		public int multiply(int x, int y) {
			
			int z = x*y;
			return z;
			
		}
		
		public int division(int x, int y) {
			
			int z = x/y;
			return z;
			
		}

	

}
